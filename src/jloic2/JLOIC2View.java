class CheckStats extends TimerTask
{
    SetupAttack s;
    JLabel pkssent;
    JLabel pkst;
    JLabel bitt;
    JLabel bits;
    CheckStats(SetupAttack s, JLabel one, JLabel two, JLabel three, JLabel four)
    {
        this.s = s;
        this.pkssent = one;
        this.pkst = two;
        this.bitt = three;
        this.bits = four;
    }
    public void run()
    {
            if (s != null)
            {
                double k= 0, w=0,j = 0;
                long y = 0;
                AttackStats stats;
                for (int x = 0; x < s.activeThreads.size(); x++)
                {
                    stats = s.activeThreads.get(x).stats;
                    k += stats.PksPerSec;
                    w += stats.KbitPerSec;
                    y += stats.PacketsSent;
                    j += stats.KbitsSent;
                }
                pkssent.setText(String.valueOf(y));
                pkst.setText(String.valueOf(k));
                bitt.setText(String.valueOf((w)));
                bits.setText(String.valueOf(j));
            }
    }
};

