    boolean CheckAll()
    {
        if (Integer.valueOf(jTextField9.getText()) < 1 || Integer.valueOf(jTextField9.getText()) > 65535)
            return false;
        if (Integer.valueOf(jTextField8.getText()) < 1 || Integer.valueOf(jTextField8.getText()) > 100)
            return false;
        if (jTextArea1.getText().length() < 1)
            return false;
        if (choice1.getSelectedIndex() < 0 || choice1.getSelectedIndex() > 1 )
            return false;
        if (Integer.valueOf(jTextField10.getText()) <  1 || Integer.valueOf(jTextField10.getText()) > 2000000000)
            return false;

        return true;
    }

    String SelectFile()
    {
        JFileChooser fc = new JFileChooser();
        JFrame frame = new JFrame();
        fc.showOpenDialog(frame);
        frame.pack();
        frame.setVisible(true);
        File file = fc.getSelectedFile();
        frame.setVisible(false);
        frame.dispose();
        frame = null;
        if (file.isFile())
        {
            return file.toString();
        }
        else
            return "";
    }
