    private void button2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button2MouseClicked
        if (CheckAll() == false)
        {
            WtfImage();
        }
        else
        {
            if (!isf.IsFiring) {
            isf.IsFiring = true;
            button2.setLabel("STOP FIRING");
            AttackInformation t = new AttackInformation(jTextArea1.getText(),
                                       Short.parseShort(jTextField9.getText()),
                                       Short.parseShort(jTextField8.getText()),
                                       jSlider1.getValue(),
                                       new Methodl4(choice1.getSelectedItem()),
                                       new Payload(jTextField5.getText(), jTextField6.getText(), jTextField7.getText()),
                                       jCheckBox1.isSelected(),jCheckBox2.isSelected(), jCheckBox3.isSelected(),
                                       Integer.valueOf(jTextField10.getText()));
            if (sa != null)
                sa.Stop();
            sa = new SetupAttack(t, isf);
            timer = new Timer();
            task = new CheckStats(sa, this.jLabel5, this.jLabel15, this.jLabel16, this.jLabel18 );
            timer.schedule( task, 100, 100 );
            sa.Start();
        } else {
            isf.IsFiring = false;
            if (sa != null)
                sa.Stop();
            task.cancel();
            timer.cancel();
            button2.setLabel("IMMA CHARGIN MAH LAZER");
        }
        }
}//GEN-LAST:event_button2MouseClicked

